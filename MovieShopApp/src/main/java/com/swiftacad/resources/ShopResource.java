package com.swiftacad.resources;

public class ShopResource {
	
	private Long id;
	private String name;
	
	public ShopResource() {
		
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	

}
